            <h3>4. TRANSFER PRICING</h3>
            <p>Transfer pricing refers to inter-company pricing arrangements, with regards to transactions between related business entities. These can include transfers of intellectual property, tangible goods, services, and loans or other financial transactions. The use of transfer pricing tax strategies has recently attracted a high level of international attention, due to the rapid rise of multinational trade, and the opening of several significant developing economies. As a result, transfer pricing has had an increased impact on corporate income taxation. As multinational corporations evolve into true global enterprises, compliance with the differing requirements of multiple overlapping tax jurisdictions has become a complicated and potentially expensive task to perform. In response to these factors, tax authorities around the world have become more aggressive in the transfer pricing arena, introducing stricter penalties, new documentation requirements, increased information exchange, improved audit staff training and increased audit and inspection activity and specialization. This intense scrutiny implies significant risks for the unprepared companies, particularly in a complex field such as transfer pricing where each transaction must be analyzed under its own unique facts and circumstances.</p>
